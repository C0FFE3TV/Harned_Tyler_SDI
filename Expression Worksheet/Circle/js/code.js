// Calculate the Circumference of a Circle
var radius = 6;
const PI = 3.14159265;

// Multiply 2, PI and the radius & assign to circ variable.
var circ = radius * 2 * PI;

// Print out the output circumference
console.log("The circumference of the circle is" +circ);